// Constructors are functions that create new objects.
// Think of them as a blueprint for the creation of new objects.

// Conventions:

// Defined with a capitalized name to distinguish them
// from other functions.

// It defines properties and behaviors instead of 
// returning a value as other functions might.

// Below is the practice:

function Dog(){
    this.name = "dogName";
    this.color = "dogColor";
    this.numLegs = 4
  }